public class Lab035_Non_Primitive_Literals {
    public static void main(String[] args) {
        // Primitive data types - these are predefined DT, which can't be broken down further
        // Primitive data types - are defined by JAVA guys
        // MAx, Min size is defined
        byte b = 10; // byte takes 1 Byte = 8 Bits
        int age = 65; // int takes 4 Byte = 32 Bits

        //Non-Primitive data types - (Defined by users), Reference data types
        // No size , max, min
        // 8 Byte, 64 Bits

        // Non-primitive data types are - String, Arrays, Classes, enum...

        System.out.println(b);
    }
}
