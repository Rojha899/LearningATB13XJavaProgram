package ex_06_Ternary_Operator;

public class Lab064_TO {
    public static void main(String[] args) {
        //Find the maximum number between 2 numbers by using ternary operator
        int x = 10;
        int y = 20;
        int max = x > y ? x : y;
        System.out.println(max);


        // System.out.println(Math.max(x,y)); // This is using predefined math.max function

    }
}
