package ex_04_Operators;

public class Lab042_Interview_Concat_Plus {
    public static void main(String[] args) {
        //CONCATINTAION - This means combining, adding or joining
        // Concatination for string it combines and for integer it performs mathematical calculation

        // String concatination example
        System.out.println("Ruchi" + "Ojha"); // o/p is displayed as RuchiOjha

        // Integer example
        int a = 10;
        int b = 20;
        System.out.println(a + b); // o/p = 30

        // + -> behave differently with the different data type.
        // + -> operator overloading



    }
}
