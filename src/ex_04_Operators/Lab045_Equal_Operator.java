package ex_04_Operators;

public class Lab045_Equal_Operator {
    public static void main(String[] args) {
        // == Compares the value (Primitive data type)
        System.out.println(10 == 10); // Answer is True
        System.out.println(10<10); // Answer is false
        System.out.println(10 == 20); // Answer is false
        System.out.println(10 >= 10); // Answer is true
    }
}
