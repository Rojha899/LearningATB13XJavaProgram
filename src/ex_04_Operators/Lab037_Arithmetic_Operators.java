package ex_04_Operators;

public class Lab037_Arithmetic_Operators {
    public static void main(String[] args) {
        //Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - it returns Remainder

        int a = 20;
        int b = 3;
        float c = 3.0F;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);

        System.out.println(a/c);

       }
}
