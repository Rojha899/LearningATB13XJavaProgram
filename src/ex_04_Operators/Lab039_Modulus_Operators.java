package ex_04_Operators;

public class Lab039_Modulus_Operators {
    public static void main(String[] args) {
        int a = 20;
        int b = 10;
        System.out.println(a%b); // Modulus prints the remainder 20/10 = 0 remainder

        System.out.println(13%7); // 13/7 = 6 remainder
    }
}
