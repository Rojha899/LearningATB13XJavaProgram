package ex_04_Operators;

public class Lab054_Interview_Ques {
    public static void main(String[] args) {

        // Character act as integers. Hence, there ASCII value will be used

        char a1 = 'A'; // ASCII value of character 'A' is - 65
        char b1 = 'B'; // ASCII value of character 'B' is - 66
        System.out.println(a1+b1); // a1+b1 = 65+66 = 131 is the answer
    }
}
