package ex_04_Operators;

public class Lab036_operators_Assignment {
    public static void main(String[] args) {
        int age = 65;
        // int -> data type
        // age -> Identifier/variable_name
        // 65 -> Literal/Variable_value
        // = -> Assignment Operator -> It assigns the RH (Right hand) value to LH (Left Hand)
        System.out.println(age);

    }
}
